package com.dzyjy.nfc.model;

public class MsgInfo {
	private String nfcChangeTime;
	private String nfcID;
	private String nfcType;
	
	private String nfcOption;
	
	public String getNfcOption() {
		return nfcOption;
	}
	public void setNfcOption(String nfcOption) {
		this.nfcOption = nfcOption;
	}
	public String getNfcChangeTime() {
		return nfcChangeTime;
	}
	public void setNfcChangeTime(String nfcChangeTime) {
		this.nfcChangeTime = nfcChangeTime;
	}
	public String getNfcID() {
		return nfcID;
	}
	public void setNfcID(String nfcID) {
		this.nfcID = nfcID;
	}
	public String getNfcType() {
		return nfcType;
	}
	public void setNfcType(String nfcType) {
		this.nfcType = nfcType;
	}
	
}
